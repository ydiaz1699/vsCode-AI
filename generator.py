def load_board(board_id: str) -> dict:
    """Load board metadata from catalog frontmatter."""
    board_file = BOARDS_DIR / f"{board_id}.md"
    if not board_file.exists():
        print(f"Error: Placa '{board_id}' no encontrada en {BOARDS_DIR}")
        print(f"Usa 'list-boards' para ver placas disponibles.")
        sys.exit(1)
    post = frontmatter.load(str(board_file))
    metadata = dict(post.metadata)
    metadata["content"] = post.content
    return metadata


def load_peripheral(peripheral_id: str) -> dict:
    """Load peripheral metadata from catalog frontmatter."""
    periph_file = PERIPHERALS_DIR / f"{peripheral_id}.md"
    if not periph_file.exists():
        print(f"Error: Periférico '{peripheral_id}' no encontrado en {PERIPHERALS_DIR}")
        print(f"Usa 'list-peripherals' para ver periféricos disponibles.")
        sys.exit(1)
    post = frontmatter.load(str(periph_file))
    metadata = dict(post.metadata)
    metadata["content"] = post.content
    return metadata


def load_all_boards() -> list:
    """Load all board metadata from catalog."""
    boards = []
    if not BOARDS_DIR.exists():
        return boards
    for f in sorted(BOARDS_DIR.glob("*.md")):
        post = frontmatter.load(str(f))
        boards.append(dict(post.metadata))
    return boards



def load_all_peripherals() -> list:
    """Load all peripheral metadata from catalog."""
    peripherals = []
    if not PERIPHERALS_DIR.exists():
        return peripherals
    for f in sorted(PERIPHERALS_DIR.glob("*.md")):
        post = frontmatter.load(str(f))
        peripherals.append(dict(post.metadata))
    return peripherals


def inject_variables(content: str, variables: dict) -> str:
    """Replace {{VARIABLE}} placeholders in content."""
    result = content
    for key, value in variables.items():
        placeholder = "{{" + key + "}}"
        result = result.replace(placeholder, str(value))
    return result


def ensure_dir(path: Path):
    """Create directory if it doesn't exist."""
    path.mkdir(parents=True, exist_ok=True)


def print_table(headers: list, rows: list):
    """Print a formatted ASCII table."""
    col_widths = [len(h) for h in headers]
    for row in rows:
        for i, cell in enumerate(row):
            col_widths[i] = max(col_widths[i], len(str(cell)))

    # Header
    header_line = " | ".join(h.ljust(col_widths[i]) for i, h in enumerate(headers))
    separator = "-+-".join("-" * col_widths[i] for i in range(len(headers)))
    print(f" {header_line}")
    print(f" {separator}")

    # Rows
    for row in rows:
        row_line = " | ".join(str(cell).ljust(col_widths[i]) for i, cell in enumerate(row))
        print(f" {row_line}")



