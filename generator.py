def cmd_new(args):
    """Generate a new project from catalog."""
    board_id = args.board
    project_name = args.name
    description = args.desc or f"Proyecto {project_name} con {board_id}"
    output_base = Path(args.output) if args.output else OUTPUT_DIR

    print(f"\n{'='*60}")
    print(f"  vsCode-AI Generator v{VERSION}")
    print(f"  Generando proyecto: {project_name}")
    print(f"{'='*60}\n")

    # Load board
    board = load_board(board_id)
    print(f"  [✓] Board: {board.get('name')} ({board.get('mcu')})")

    # Load peripherals
    peripherals = []
    if args.peripheral:
        for p_id in args.peripheral:
            periph = load_peripheral(p_id)
            peripherals.append(periph)
            print(f"  [✓] Periférico: {periph.get('name')} ({periph.get('protocol', 'N/A')})")

    # Create project directory
    project_dir = output_base / project_name
    if project_dir.exists():
        print(f"\n  [!] El directorio ya existe: {project_dir}")
        print(f"      Usa otro nombre o elimina el directorio existente.")
        sys.exit(1)

    print(f"\n  Creando estructura en: {project_dir}\n")


    # Create folder structure
    folders = [
        project_dir / ".ai",
        project_dir / "src",
        project_dir / "include",
        project_dir / "lib",
        project_dir / "test",
        project_dir / "docs",
        project_dir / ".vscode",
    ]
    for folder in folders:
        ensure_dir(folder)
        print(f"  [+] {folder.relative_to(project_dir)}/")

    # Template variables
    variables = {
        "PROJECT_NAME": project_name,
        "BOARD_ID": board_id,
        "BOARD_NAME": board.get("name", "Unknown"),
        "MCU": board.get("mcu", "Unknown"),
        "PLATFORM": board.get("platform", "espressif32"),
        "FRAMEWORK": board.get("framework", "arduino"),
        "DATE": datetime.now().strftime("%Y-%m-%d"),
        "DATETIME": datetime.now().isoformat(),
        "VERSION": VERSION,
        "DESCRIPTION": description,
        "WIFI": "true" if board.get("wifi") else "false",
        "BLUETOOTH": "true" if board.get("bluetooth") else "false",
        "FLASH": board.get("flash", "N/A"),
        "RAM": board.get("ram", "N/A"),
    }

    # Generate .ai/ files
    print("\n  Generando contexto IA (.ai/)...")

    ai_files = {
        "PROJECT_CONTEXT.md": generate_project_context(project_name, board, description),
        "ARCHITECTURE.md": generate_architecture(project_name, board),
        "PROTOCOL.md": generate_protocol(),
    }

    for filename, content in ai_files.items():
        filepath = project_dir / ".ai" / filename
        filepath.write_text(inject_variables(content, variables), encoding="utf-8")
        print(f"  [+] .ai/{filename}")

    # Generate platformio.ini
    print("\n  Generando configuración PlatformIO...")
    pio_content = generate_platformio_ini(board, peripherals)
    (project_dir / "platformio.ini").write_text(pio_content, encoding="utf-8")
    print(f"  [+] platformio.ini")

    # Generate src/main.cpp
    print("\n  Generando código fuente...")
    main_content = generate_main_cpp(board, project_name)
    (project_dir / "src" / "main.cpp").write_text(main_content, encoding="utf-8")
    print(f"  [+] src/main.cpp")

    # Generate secrets.h.template if WiFi
    if board.get("wifi", False):
        secrets_content = generate_secrets_template()
        (project_dir / "include" / "secrets.h.template").write_text(secrets_content, encoding="utf-8")
        print(f"  [+] include/secrets.h.template")

    # Copy .vscode/ files if available
    vscode_src = ENVIRONMENTS_DIR / "vscode"
    if vscode_src.exists():
        print("\n  Copiando configuración VSCode...")
        for f in vscode_src.iterdir():
            if f.is_file():
                shutil.copy2(f, project_dir / ".vscode" / f.name)
                print(f"  [+] .vscode/{f.name}")
    else:
        # Generate default .vscode files
        extensions_json = '{\n    "recommendations": [\n        "platformio.platformio-ide"\n    ]\n}\n'
        (project_dir / ".vscode" / "extensions.json").write_text(extensions_json, encoding="utf-8")
        print(f"  [+] .vscode/extensions.json (default)")

    # Copy tooling files if available
    tooling_src = ENVIRONMENTS_DIR / "tooling"
    if tooling_src.exists():
        print("\n  Copiando archivos de tooling...")
        for f in tooling_src.iterdir():
            if f.is_file():
                shutil.copy2(f, project_dir / f.name)
                print(f"  [+] {f.name}")


    # Generate project README
    readme_content = generate_project_readme(project_name, board, description)
    (project_dir / "README.md").write_text(readme_content, encoding="utf-8")
    print(f"  [+] README.md")

    # Generate .gitignore for project
    gitignore_content = """# PlatformIO
.pio/
.pioenvs/
.piolibdeps/

# IDE
.vscode/.browse.c_cpp.db*
.vscode/c_cpp_properties.json
.vscode/launch.json
.vscode/ipch

# Secrets
include/secrets.h

# OS
.DS_Store
Thumbs.db
"""
    (project_dir / ".gitignore").write_text(gitignore_content, encoding="utf-8")
    print(f"  [+] .gitignore")

    # Summary
    print(f"\n{'='*60}")
    print(f"  ✅ Proyecto generado exitosamente!")
    print(f"{'='*60}")
    print(f"  📁 Ubicación: {project_dir}")
    print(f"  🔧 Board: {board.get('name')} ({board.get('mcu')})")
    if peripherals:
        print(f"  📡 Periféricos: {', '.join(p.get('name', p.get('id')) for p in peripherals)}")
    print(f"\n  Próximos pasos:")
    print(f"  1. cd {project_dir}")
    if board.get("wifi"):
        print(f"  2. cp include/secrets.h.template include/secrets.h")
        print(f"  3. Edita secrets.h con tus credenciales")
        print(f"  4. pio run --target upload")
    else:
        print(f"  2. pio run --target upload")
    print()


def cmd_add_peripheral(args):
    """Add a peripheral to an existing project."""
    project_path = Path(args.project).resolve()
    peripheral_id = args.peripheral

    if not project_path.exists():
        print(f"Error: Proyecto no encontrado en {project_path}")
        sys.exit(1)

    pio_ini = project_path / "platformio.ini"
    if not pio_ini.exists():
        print(f"Error: No se encontró platformio.ini en {project_path}")
        print(f"       ¿Es un proyecto PlatformIO válido?")
        sys.exit(1)

    # Load peripheral
    periph = load_peripheral(peripheral_id)
    lib = periph.get("library", periph.get("lib_deps", ""))

    print(f"\n  Agregando periférico: {periph.get('name')}")
    print(f"  Protocolo: {periph.get('protocol', 'N/A')}")
    print(f"  Categoría: {periph.get('category', 'N/A')}")

    if lib and lib != "(nativo)":
        # Read current platformio.ini
        content = pio_ini.read_text(encoding="utf-8")

        if lib in content:
            print(f"\n  [!] La librería '{lib}' ya está en platformio.ini")
            return

        # Add lib_deps
        if "lib_deps" in content:
            # Append to existing lib_deps
            content = content.replace("lib_deps = \n", f"lib_deps = \n    {lib}\n", 1)
            if f"    {lib}" not in content:
                # Try alternate format
                lines = content.split("\n")
                for i, line in enumerate(lines):
                    if line.strip().startswith("lib_deps"):
                        # Find last lib_dep line
                        j = i + 1
                        while j < len(lines) and lines[j].startswith("    "):
                            j += 1
                        lines.insert(j, f"    {lib}")
                        content = "\n".join(lines)
                        break
        else:
            content += f"\nlib_deps = \n    {lib}\n"

        pio_ini.write_text(content, encoding="utf-8")
        print(f"  [✓] Agregado a platformio.ini: {lib}")
    else:
        print(f"  [i] Este periférico no requiere librería externa (uso nativo GPIO)")

    print(f"\n  ✅ Periférico '{periph.get('name')}' agregado al proyecto.")
    print()



