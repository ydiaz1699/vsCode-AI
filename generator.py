    parser_new.add_argument("--board", required=True, help="ID de la placa (ver list-boards)")
    parser_new.add_argument("--name", required=True, help="Nombre del proyecto")
    parser_new.add_argument(
        "--peripheral", action="append", default=[],
        help="ID de periférico a incluir (puede repetirse)"
    )
    parser_new.add_argument("--desc", help="Descripción del proyecto")
    parser_new.add_argument("--output", help="Directorio de salida (default: outputs/)")
    parser_new.set_defaults(func=cmd_new)

    # === add-peripheral ===
    parser_add = subparsers.add_parser("add-peripheral", help="Agregar periférico a proyecto existente")
    parser_add.add_argument("--project", required=True, help="Ruta al proyecto")
    parser_add.add_argument("--peripheral", required=True, help="ID del periférico")
    parser_add.set_defaults(func=cmd_add_peripheral)

    # === list-boards ===
    parser_lb = subparsers.add_parser("list-boards", help="Listar placas disponibles")
    parser_lb.set_defaults(func=cmd_list_boards)

    # === list-peripherals ===
    parser_lp = subparsers.add_parser("list-peripherals", help="Listar periféricos disponibles")
    parser_lp.set_defaults(func=cmd_list_peripherals)

    # === version ===
    parser_v = subparsers.add_parser("version", help="Mostrar versión")
    parser_v.set_defaults(func=cmd_version)

