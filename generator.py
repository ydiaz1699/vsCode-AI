def generate_platformio_ini(board: dict, peripherals: list) -> str:
    """Generate platformio.ini content based on board metadata."""
    platform = board.get("platform", "espressif32")
    board_id = board.get("board", "esp32dev")
    framework = board.get("framework", "arduino")

    lines = [
        "; PlatformIO Project Configuration",
        f"; Generated by vsCode-AI Generator v{VERSION}",
        f"; Board: {board.get('name', board.get('id', 'unknown'))}",
        f"; Date: {datetime.now().strftime('%Y-%m-%d')}",
        "",
        "[env:default]",
        f"platform = {platform}",
        f"board = {board_id}",
        f"framework = {framework}",
        "monitor_speed = 115200",
    ]

    # Build flags for ESP32
    if "esp32" in platform.lower():
        lines.append("build_flags = ")
        lines.append("    -DCORE_DEBUG_LEVEL=3")
        if board.get("wifi", False):
            lines.append("    -DWIFI_ENABLED=1")

    # Library dependencies from peripherals
    lib_deps = []
    for periph in peripherals:
        lib = periph.get("library", periph.get("lib_deps", ""))
        if lib and lib != "(nativo)":
            lib_deps.append(lib)

    if lib_deps:
        lines.append("lib_deps = ")
        for lib in lib_deps:
            lines.append(f"    {lib}")

    lines.append("")
    return "\n".join(lines)



def generate_main_cpp(board: dict, project_name: str) -> str:
    """Generate src/main.cpp skeleton based on board capabilities."""
    lines = [
        f"/**",
        f" * {project_name}",
        f" * Generated by vsCode-AI Generator v{VERSION}",
        f" * Board: {board.get('name', 'Unknown')}",
        f" * Date: {datetime.now().strftime('%Y-%m-%d')}",
        f" */",
        "",
        "#include <Arduino.h>",
    ]

    if board.get("wifi", False):
        mcu = board.get("mcu", "").lower()
        if "esp32" in mcu:
            lines.append("#include <WiFi.h>")
        elif "esp8266" in mcu:
            lines.append("#include <ESP8266WiFi.h>")
        lines.append('#include "secrets.h"')

    lines.extend([
        "",
        "// === CONFIGURATION ===",
        "#define LED_BUILTIN_PIN 2",
        "",
        "// === GLOBAL VARIABLES ===",
        "",
        "// === FUNCTION PROTOTYPES ===",
        "void initHardware();",
    ])

    if board.get("wifi", False):
        lines.append("void initWiFi();")

    lines.extend([
        "",
        "// === SETUP ===",
        "void setup() {",
        "    Serial.begin(115200);",
        "    delay(1000);",
        f'    Serial.println("\\n[{project_name}] Iniciando...");',
        "",
        "    initHardware();",
    ])

    if board.get("wifi", False):
        lines.append("    initWiFi();")

    lines.extend([
        "",
        f'    Serial.println("[{project_name}] Sistema listo.");',
        "}",
        "",
        "// === LOOP ===",
        "void loop() {",
        "    // TODO: Implementar lógica principal",
        "    delay(1000);",
        "}",
        "",
        "// === IMPLEMENTATIONS ===",
        "void initHardware() {",
        "    pinMode(LED_BUILTIN_PIN, OUTPUT);",
        "    digitalWrite(LED_BUILTIN_PIN, LOW);",
        '    Serial.println("[HW] Hardware inicializado.");',
        "}",
    ])

    if board.get("wifi", False):
        lines.extend([
            "",
            "void initWiFi() {",
            '    Serial.print("[WiFi] Conectando...");',
            "    WiFi.begin(WIFI_SSID, WIFI_PASS);",
            "",
            "    int attempts = 0;",
            "    while (WiFi.status() != WL_CONNECTED && attempts < 20) {",
            "        delay(500);",
            '        Serial.print(".");',
            "        attempts++;",
            "    }",
            "",
            "    if (WiFi.status() == WL_CONNECTED) {",
            "        Serial.println();",
            '        Serial.print("[WiFi] Conectado. IP: ");',
            "        Serial.println(WiFi.localIP());",
            "    } else {",
            '        Serial.println("\\n[WiFi] Error de conexión.");',
            "    }",
            "}",
        ])

    lines.append("")
    return "\n".join(lines)



